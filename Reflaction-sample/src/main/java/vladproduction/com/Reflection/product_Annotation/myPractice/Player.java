package Reflection.product_Annotation.myPractice;

public class Player {
    private String name;
    @MyValidator(errorMessage = "number should be > 0")
    //@MyValidator
    private int number;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    @Override
    public String toString() {
        return "Player{" +
                "name='" + name + '\'' +
                ", number=" + number +
                '}';
    }
}
