package vladproduction.com.app01;

public class IntegerPair {
    int x;
    int y;

    public IntegerPair(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "IntegerPair{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
