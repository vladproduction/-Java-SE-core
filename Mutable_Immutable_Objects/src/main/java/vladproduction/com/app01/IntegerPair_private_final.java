package vladproduction.com.app01;

public class IntegerPair_private_final {
    private final int x;
    private final int y;

    public IntegerPair_private_final(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "IntegerPair{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
