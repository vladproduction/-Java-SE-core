package vladproduction.com.app01;

public class JavaDataTypesPrimitive2 {
    public static void main(String[] args) {

        System.out.println("Java data types");
        System.out.println();
        System.out.println("\tJava data types primitive:");
        int i = 4 , j = i;
        System.out.println("i = " + i + "; j = " + j);
        System.out.println("increment j: ");
        ++j;
        System.out.println("i = " + i);
        System.out.println("j = " + j);
        System.out.println("operator: i + j");
        System.out.println("res = " + i + j);

    }
}
